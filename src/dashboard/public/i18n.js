const _LANG_KEY = 'orchestos-lang';
const _LANG_DEFAULT = 'en';

function getLang() {
  try { return localStorage.getItem(_LANG_KEY) || _LANG_DEFAULT; } catch { return _LANG_DEFAULT; }
}

function setLang(lang) {
  try { localStorage.setItem(_LANG_KEY, lang); } catch {}
}

/**
 * Translate a key. Interpolates {0}, {1}, … with extra args.
 * Falls back to English, then to the key itself.
 */
function t(key, ...args) {
  const lang = getLang();
  const dict = I18N[lang] || I18N[_LANG_DEFAULT];
  let str = dict[key];
  if (str === undefined) str = (I18N[_LANG_DEFAULT] || {})[key];
  if (str === undefined) str = key;
  return args.reduce((s, a, i) => s.replace(`{${i}}`, a), str);
}

// Expose on window so any script can call t() after a naming conflict
window.t = t;
window.getLang = getLang;
window.setLang = setLang;
